#define NUM_TEST_POINTS (sizeof(TEST_POINTS) / sizeof(TEST_POINTS[0]))

/* Precomputed bounding sphere of the test points calculated with a reference
 * implementation of the iterative Ritter algorithm, run for 100 iterations. */
const float TEST_POINTS_EXPECTED_CENTRE[3] = { -42.6978f, -18.8544f, 12.8491f };
const float TEST_POINTS_EXPECTED_RADIUS = 253.866f;

/* True if no point in a point set is outside (centre, radius) by more than the
 * tolerance value. */
static int points_in_sphere(
	const float *points, 
	unsigned num_points,
	unsigned ndim,
	const unsigned *index, 
	const float *centre, 
	float radius, 
	float tolerance)
{
	float radius_squared = radius * radius;
	float max_error = 0.0f;
	for (unsigned i = 0; i < num_points; ++i) {
		const float *p = points + ndim * (index ? index[i] : i);

		float distance_squared = 0.0f;
		for (unsigned j = 0; j < ndim; ++j) {
			float d = p[j] - centre[j];
			distance_squared += d * d;
		}

		if (distance_squared > radius_squared) {
			float distance = sqrtf(distance_squared);
			float error = distance - radius;
			if (error > max_error)
				max_error = error;
		}
	}
	return max_error <= tolerance;
}

static void test_bounding_sphere(void)
{
	srand(0);

	/* Test without an index. The result should be reasonably close to the
	 * computed sphere. */
	float center[3];
	float radius = bounding_sphere(center, TEST_POINTS[0], NUM_TEST_POINTS, 
		sizeof(float[3]), 3, NULL, 100);
	fprintf(stdout, "center: (%.3f, %.3f, %.3f) radius: %.3f\n", 
		center[0], center[1], center[2], radius);
	if (radius - TEST_POINTS_EXPECTED_RADIUS > 0.05f)
		abort();

	/* The sphere must bound the points. */
	if (!points_in_sphere(TEST_POINTS[0], NUM_TEST_POINTS, 3, NULL, center, radius, 1e-4f))
		abort();

	/* Test random subsets of the points. */
	unsigned index[NUM_TEST_POINTS];
	for (unsigned i = 0; i < NUM_TEST_POINTS; ++i)
		index[i] = i;

	for (unsigned i = 0; i < 10000; ++i) {
		/* Shuffle. */
		for (unsigned j = NUM_TEST_POINTS - 1; j != 0; --j) {
			unsigned k = rand() % (j + 1);
			unsigned t = index[j];
			index[j] = index[k];
			index[k] = t;
		}

		/* Bound a random prefix of the shuffled points. */
		unsigned num_points = rand() % NUM_TEST_POINTS;
		unsigned num_iterations = rand() % 9;
		for (unsigned d = 1; d <= 3; ++d) {
			radius = bounding_sphere(center, TEST_POINTS[0], num_points, 
				d * sizeof(float), d, index, num_iterations);
			if (!points_in_sphere(TEST_POINTS[0], num_points, d, index, 
				center, radius, 1e-4f))
				abort();
		}
	}
}

